// Probe: verify the BydbQL codegen produces a string that BanyanDB's parser
// accepts (no more `ORDER BY value` parse error) and that the result view
// renders with the item-level timestamp fallback when the list-level is null.

import { chromium } from '@playwright/test';
import { mkdirSync } from 'node:fs';

const SHOTS = '/tmp/topn-bydbql-shots';
mkdirSync(SHOTS, { recursive: true });

(async () => {
  const browser = await chromium.launch({ headless: true });
  const context = await browser.newContext({ viewport: { width: 1440, height: 1100 } });
  await context.addCookies([{
    name: 'session',
    value: 'qh0%2BvlDVthW%2BtOrvbwfsJ9o9Km3%2FJeNVW8EhvuXQbseC66m%2FLAXMTQNacSxbn80jEGkDMARnlFvlrDd4r3aqrJ2VVbiWRKb62CTFZ56p9HHWnhzA%2FiK63Q%3D%3D%3B4EDjQyp5TtVyGRvhVmtbpQTJ8zNp8LlF',
    domain: 'localhost', path: '/', httpOnly: true, sameSite: 'Lax',
  }]);
  const page = await context.newPage();
  page.on('console', (m) => { if (m.type() === 'error') console.log('[browser:error]', m.text()); });

  // 1. Build a Top-N query in the visual builder for the user's exact case
  await page.goto('http://localhost:4000/query');
  await page.waitForSelector('.qb-card', { timeout: 30_000 });
  await page.evaluate(() => localStorage.clear());
  await page.reload();
  await page.waitForSelector('.qb-card', { timeout: 30_000 });
  await page.waitForTimeout(1000);

  await page.locator('button.qb-cat-btn', { hasText: /^top-n$/i }).first().click();
  await page.waitForTimeout(800);
  await page.locator('select[aria-label="Group"]').selectOption('sw_metric');
  await page.waitForTimeout(1500);
  await page.locator('select[aria-label="Resource"]').selectOption('top_service');
  await page.waitForTimeout(300);
  const timeAcc = page.locator('.qb-acc-head', { hasText: /^TIME/i }).first();
  if (await timeAcc.count() > 0 && (await timeAcc.getAttribute('aria-expanded')) === 'false') {
    await timeAcc.click();
    await page.waitForTimeout(200);
  }
  await page.locator('select[aria-label="Relative time range"]').selectOption('-7d');
  await page.waitForTimeout(300);

  // 2. Capture the BydbQL string the builder produces
  const genLine = (await page.locator('.qb-gen-line').textContent())?.replace(/\s+/g, ' ').trim();
  console.log('Generated BydbQL:', genLine);

  // 3. Eject to code mode
  await page.locator('button.qb-eject').click();
  await page.waitForTimeout(500);
  const codeText = await page.locator('textarea').first().inputValue();
  console.log('Code-mode BydbQL:');
  console.log(codeText);

  // 4. Run from code mode (this goes to /v1/bydbql/query, NOT /v1/measure/topn)
  // The code-mode Run button is in .qb-foot .qb-foot-row (not the builder rail).
  await page.locator('.qb-foot .btn-primary').click();
  await page.waitForTimeout(3000);
  const errorText = await page.locator('.qb-error, .result-status .rs.fail').first().textContent().catch(() => null);
  console.log('Run result:', errorText ?? '(no error visible)');
  await page.screenshot({ path: `${SHOTS}/01-code-mode-run.png`, fullPage: true });

  // 5. Go back to builder and run (this goes to /v1/measure/topn)
  await page.locator('button.qb-mode-btn', { hasText: /builder/i }).first().click();
  await page.waitForTimeout(800);
  await page.locator('.qb-foot .btn-primary, .qb-rail .btn-primary').first().click();
  await page.waitForSelector('.tnlb-bar', { timeout: 30_000 });
  await page.waitForTimeout(800);
  await page.screenshot({ path: `${SHOTS}/02-builder-mode-result.png`, fullPage: true });

  // 6. Inspect the result — the leaderboard should still render
  const rowCount = await page.locator('.tnlb-row').count();
  const firstCell = (await page.locator('.tnlb-ent-v').first().textContent())?.trim();
  console.log(`\n=== result ===`);
  console.log(`leaderboard rows: ${rowCount}`);
  console.log(`first cell value: ${firstCell}`);

  const checks = [];
  checks.push({ label: 'codegen emits "ORDER BY DESC" (no `value`)', pass: !!genLine && /ORDER BY DESC(?!.*value)/.test(genLine) });
  checks.push({ label: 'codegen does NOT emit LIMIT or OFFSET (Top-N grammar has neither)', pass: !genLine?.includes('LIMIT') && !genLine?.includes('OFFSET') });
  checks.push({ label: 'codegen does NOT emit "ORDER BY value" anymore', pass: !genLine?.includes('ORDER BY value') });
  checks.push({ label: 'code-mode BydbQL string parses (no parse error banner)', pass: !errorText?.includes('syntax error') });
  checks.push({ label: 'builder-mode run still renders leaderboard rows', pass: rowCount > 0 });
  checks.push({ label: 'first cell value is a plain decoded identifier (not gibberish)', pass: !!firstCell && !firstCell.match(/[ɕΞ¥¬é]/) && firstCell.length > 0 });

  console.log('\n=== final ===');
  let allPass = true;
  for (const c of checks) {
    console.log(`  ${c.pass ? '✓' : '✗'}  ${c.label}`);
    if (!c.pass) allPass = false;
  }
  console.log(allPass ? '\nALL PASS' : '\nFAIL');

  await browser.close();
  process.exit(allPass ? 0 : 1);
})();